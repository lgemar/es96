function trace3d(w,r,dth,N);
% trace3d(w,r,dth,N);
%figure;
mw = .25;
mirror3d( 0, 0, 1, mw, r );
hold on;
mirror3d( w, 0, -1, mw, r );
lense3d( w+1, 0, r, 3*r);
colormap(blues);
alpha(.6);
cube3d([-1.75 (-.125) (.9-.125) ], [.5 .25 .25]);
plot3([-1.25 mw],[0 0],[.9 .9],'r');
cube3d([ w+3 (-.125) (-.125) ],.25);
h = ray3d( mw, w-mw, 0, .9*r, dth, 0, N, w+1, w+3 );
set(h,'color',[1 0 0]);
%set(gca,'visible','off');
set(gcf,'color',[.75 .75 1]);
camlight left;
camlight right;
camlight headlight;
hold off;
